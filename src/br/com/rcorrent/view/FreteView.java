    private void jbGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGravarActionPerformed
        try {
            Frete freteGravar = getFrete();
            //testa se a FRETE veio completa, com todos os itens a serem salvos
            if(freteGravar == null){
                return;
            }
            //testa se está ocorrendo uma gravação
            if(this.estado == Estados.getInsercao().getEstado()){
                freteDao.inserirFrete(freteGravar);
                jtfFreteNumeroDocumento.setText(freteGravar.getCodigoFrete().toString());
                JOptionPane.showMessageDialog(null, "Frete cadastrado com sucesso!");

                //setar nr documento no pedágio
                jtfPedagioNumDocumento.setText(freteGravar.getCodigoFrete().toString());

            }
            //testa se está ocorrendo um update ou uma alteração
            if(this.estado == Estados.getAlteracao().getEstado()){
                freteDao.alterarFrete(freteGravar);
                JOptionPane.showMessageDialog(null, "Registro de Frete alterado com sucesso!");
            }

            //deixar os botões em estado PADRAO
            estadoPadraoDosBotoes();

            //desabilitar os campos
            habilitarCampos(false);

            //habilitar aba precificação
            jTabbedPane1.setEnabledAt(1, true);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar Frete");
        } finally {
            freteDao.fecharCursores();
        }
    }//GEN-LAST:event_jbGravarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        try {
            Frete freteDeletar = getFrete();
            //tratar (se o FRETE veio com todos os campos corretos ou se ela veio por completo.
                if (freteDeletar == null) {
                    JOptionPane.showMessageDialog(null, "Selecione um FRETE para excluir");
                    return;
                }
                //criando uma optionPane para confirmar a exclusão
                if (JOptionPane.showConfirmDialog(null, "Deseja Realment exlcuir o Frete?", "ATENÇÃO", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    freteDao.excluirFrete(freteDeletar);
                    JOptionPane.showMessageDialog(null, "Frete excluído com sucesso!!!");
                    limparCampo();
                    habilitarCampos(false);
                    //desabilitar botões
                    estadoInicialDosBotoes();
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao excluir Frete");
            } finally {
                freteDao.fecharCursores();
            }
    }//GEN-LAST:event_jbExcluirActionPerformed

