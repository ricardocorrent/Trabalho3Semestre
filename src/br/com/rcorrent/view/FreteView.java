    public Boolean validarFrete(){
        try {
            if(jtfFreteDescricao.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Informe o Produto");
                jtfFreteDescricao.grabFocus();
                return false;
            }
            if(jftfFretePeso.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Informe o Peso do Produto");
                jftfFretePeso.grabFocus();
                return false;
            }
            if(jtfFretePrecoTonelada.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Informe o Preço a ser pago por Tonelada");
                jtfFretePrecoTonelada.grabFocus();
                return false;
            }            
            if(veiculo == null){
                JOptionPane.showMessageDialog(null, "Informe um Veículo para o Frete");
                return false;
            }
            if(enderecoOrigem == null){
                JOptionPane.showMessageDialog(null, "Informe uma Origem para o Frete");
                return false;
            }
            if(enderecoDestino == null){
                JOptionPane.showMessageDialog(null, "Informe um Destino para o Frete");
                return false;
            }
            return true;
        } catch (Exception e) {
            e.printStackTrace();
            return false;
        }
    }
    
    public Frete getFrete(){
        try {
            Frete freteGet = new Frete();
            
            if(!jtfFreteNumeroDocumento.getText().isEmpty()){
                freteGet.setCodigoFrete(Long.valueOf(jtfFreteNumeroDocumento.getText()));
            }
            
            freteGet.setDataFrete(DataUtils.stringToDateTimeYY(jftfDataFrete.getText()));
            
            freteGet.setDescricaoProduto(jtfFreteDescricao.getText());
            freteGet.setQuantidadeProduto(Double.valueOf(jftfFretePeso.getText()));
            freteGet.setValorPeso(Double.valueOf(jtfFretePrecoTonelada.getText()));
            
            freteGet.setVeiculo(this.veiculo);
            freteGet.setEnderecoOrigem(this.enderecoOrigem);
            freteGet.setEnderecoDestino(this.enderecoDestino);
            freteGet.setPrecoDiesel(this.diesel);
            freteGet.setValorFrete(Double.valueOf(jtfCustoTotal.getText().replace(",", ".")));
            freteGet.setDistancia(Double.valueOf(jtfPrecoDistancia.getText().replace(",", ".").replace(" Km", "")));
            
            this.frete = freteGet;
            return freteGet;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
    
