    /**SET MARCA.
     * Ao ser 'chamada', ela carrega NA JFORM as informações de uma marca
     * 
     */
    public void setMarca(Marca marca){
        try {
            jtfCodigo.setText(marca.getCodigoMarca().toString());
            jtfNome.setText(marca.getNomeMarca());
            jtfSigla.setText(marca.getAbreviaturaMarca());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    /**GET MARCA.
     * Método para carregar uma MARCA
     * com informações da JForm completada
     * @return 
     */
    public Marca getMarca(){
        try {
            marca = new Marca();
            if(!jtfCodigo.getText().isEmpty()){
                marca.setCodigoMarca(Long.valueOf(jtfCodigo.getText()));
            }
            if(!validarMarca()){
                return null;
            } else {
                marca.setNomeMarca(jtfNome.getText());
                marca.setAbreviaturaMarca(jtfSigla.getText());            
                return marca;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        } 
        
    }
    
    /**VALIDAR CAMPOS.
     * Método que faz a validação dos campos
     * se todos estiverem preenchidos, OK
     */
    public Boolean validarMarca(){
        if(jtfNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe um Nome");
            jtfNome.grabFocus();
            return false;
        }
        
        if(jtfSigla.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe uma Abreviatura");
            jtfSigla.grabFocus();
            return false;
        }
        return true;
    }
    
