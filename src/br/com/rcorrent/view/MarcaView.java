    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        try {
           Marca marcaDeletar = getMarca();
           //tratar (se a MARCA veio com todos os campos corretos ou se ela veio por completo.
            if (marcaDeletar == null) {
                JOptionPane.showMessageDialog(null, "Selecione uma Marca para excluir");
                return;
            }
            //criando uma optionPane para confirmar a exclusão
            if (JOptionPane.showConfirmDialog(null, "Deseja Realment exlcuir a Marca?", "ATENÇÃO", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                marcaDao.excluirMarca(marcaDeletar);
                JOptionPane.showMessageDialog(null, "Cidade excluída com sucesso!!!");
                limparCampo();
                habilitarCampos(false);
                //desabilitar botões
                estadoInicialDosBotoes();
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao excluir cidade");
        } finally {
            marcaDao.fecharCursores();
            preencherTabelaMarca();
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGravarActionPerformed
        try {
            Marca marcaGravar = getMarca();
            //testa se a MARCA veio completa, com todos os itens a serem salvos
            if(marcaGravar == null){
                return;
            }
            //testa se está ocorrendo uma gravação
            if(this.estado == Estados.getInsercao().getEstado()){
                marcaDao.inserirMarca(marcaGravar);
                jtfCodigo.setText(marcaGravar.getCodigoMarca().toString());
                JOptionPane.showMessageDialog(null, "Marca cadastrada com sucesso!");
            }
            //testa se está ocorrendo um update ou uma alteração
            if(this.estado == Estados.getAlteracao().getEstado()){
                marcaDao.alterarMarca(marcaGravar);
                JOptionPane.showMessageDialog(null, "Registro de Marca alterado com sucesso!");
            }        
            
            //deixar os botões em estado PADRAO
            estadoPadraoDosBotoes();
            
            //desabilitar os campos
            habilitarCampos(false);
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar Marca..");
        } finally {
            marcaDao.fecharCursores();
            limparCampo();
            preencherTabelaMarca();
        }
    }//GEN-LAST:event_jbGravarActionPerformed

