    private void jbPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPesquisarActionPerformed
        List<String> campos = new ArrayList<>();
        campos.add("CD_MARCA");
        campos.add("NM_MARCA");
        campos.add("AB_MARCA");
        
        switch(jcbFiltrar.getSelectedIndex()){
            case 0:{//caso seja escolhido a opção GERAL
                preencherTabelaMarca();
            } break;
            case 1:{//caso seja escolhido a opção CÓDIGO
                //testar para valores numéricos
                try {
                    JTableUtils.preencherJTable(jtConsultar, marcaDao.resultSetGetByIdMarca(Long.parseLong(jtfValor.getText())), campos);                    
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Informe um valor numérico");
                }
            }break;
            case 2:{//caso seja escolhido a opção MARCA
                try {
                    JTableUtils.preencherJTable(jtConsultar, marcaDao.resultSetGetByNomeMarca(jtfValor.getText()), campos);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }break;
            case 3:{//caso seja escolhido a opção ABREVIATURA
                try {
                    JTableUtils.preencherJTable(jtConsultar, marcaDao.resultSetGetByAbreviaturaMarca(jtfValor.getText()), campos);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }break;
        }
        marcaDao.fecharCursores();
    }//GEN-LAST:event_jbPesquisarActionPerformed

