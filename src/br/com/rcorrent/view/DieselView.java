    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        try {
            Diesel dieselDeletar = getDiesel();
            //tratar (se a MARCA veio com todos os campos corretos ou se ela veio por completo.
                if (dieselDeletar == null) {
                    JOptionPane.showMessageDialog(null, "Selecione um Diesel para excluir");
                    return;
                }
                //criando uma optionPane para confirmar a exclusão
                if (JOptionPane.showConfirmDialog(null, "Deseja Realment exlcuir o Diesel?", "ATENÇÃO", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    dieselDao.excluirDiesel(dieselDeletar);
                    JOptionPane.showMessageDialog(null, "Diesel excluído com sucesso!!!");
                    limparCampo();
                    habilitarCampos(false);
                    //desabilitar botões
                    estadoInicialDosBotoes();
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao excluir Diesel");
            } finally {
                dieselDao.fecharCursores();
            }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGravarActionPerformed
        try {
            Diesel dieselGravar = getDiesel();
            //testa se o DIESEL veio completa, com todos os itens a serem salvos
            if(dieselGravar == null){
                return;
            }
            //testa se está ocorrendo uma gravação
            if(this.estado == Estados.getInsercao().getEstado()){
                dieselDao.inserirDiesel(dieselGravar);
                JOptionPane.showMessageDialog(null, "Preço do Diesel cadastrado com sucesso!");
            }
            //testa se está ocorrendo um update ou uma alteração
            if(this.estado == Estados.getAlteracao().getEstado()){
                Date novaData = DataUtils.stringToDateTimeYY(jftfDataDiesel.getText());
                dieselDao.alterarDiesel(dieselAlterar, novaData);
                JOptionPane.showMessageDialog(null, "Registro de Preço do Diesel alterado com sucesso!");
            }

            //deixar os botões em estado PADRAO
            estadoPadraoDosBotoes();

            //desabilitar os campos
            habilitarCampos(false);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar Diesel");
        } finally {
            dieselDao.fecharCursores();
            limparCampo();
        }
    }//GEN-LAST:event_jbGravarActionPerformed

