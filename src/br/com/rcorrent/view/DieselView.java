    public Diesel getDiesel(){
        try {
            diesel = new Diesel();            
            diesel.setDataPreco(DataUtils.stringToDateTime(jftfDataDiesel.getText()));
            diesel.setValorDiesel(Double.valueOf(jtfValorCombustivel.getText()));
            return diesel;
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
    
    public void setDiesel(Diesel diesel){
        try {
            jftfDataDiesel.setText(DataUtils.dateTimeToString(diesel.getDataPreco()));
            jtfValorCombustivel.setText(String.valueOf(diesel.getValorDiesel()));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
